using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace nilnul.num.border._duo_.upper
{
	public class ToClosed
	{
		static public border.vow_.close.Ed Eval(nilnul.num.Border1 border) {
			if (border.beClose)
			{
				return new vow_.close.Ed(border);
			}
			else
			{
				return new vow_.close.Ed(
					border.mark-1
				);
			}
		}
		public border.vow_.close.Ed eval(nilnul.num.Border1 border) {
			if (border.beClose)
			{
				return new vow_.close.Ed(border);
			}
			else
			{
				return new vow_.close.Ed(
					border.mark-1
				);
			}
		}
	}
}
