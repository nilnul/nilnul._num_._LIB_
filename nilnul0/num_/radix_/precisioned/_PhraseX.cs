using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace nilnul.num_.radix_.precisioned
{
	/// <summary>
	/// most significant bit first;
	/// </summary>
	static public class _PhraseX
	{
		static public string _Phrase_1valid(num_.radix_.Precisioned radix, char[] alphabet)
		{
			return $"{num_.radix._PhraseX._Phrase_1valid(radix.gits,alphabet)}*{radix.ponented}";
		}

		static public string Phrase(num_.radix_.Precisioned radix)
		{
			return $"{num_.radix._PhraseX.Phrase(radix.gits,radix.radic)}*{radix.ponented}";
		}
	}
}
